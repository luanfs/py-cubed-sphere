####################################################################################
#
# Module to compute the CFL number
####################################################################################
import numpy as np

####################################################################################
# CFL number in x direction
####################################################################################
def cfl_x(u_edges, cs_grid, simulation):
    cx = u_edges*simulation.dt/cs_grid.dx
    return cx

####################################################################################
# CFL number in y direction
####################################################################################
def cfl_y(v_edges, cs_grid, simulation):
    cy = v_edges*simulation.dt/cs_grid.dy
    return cy
